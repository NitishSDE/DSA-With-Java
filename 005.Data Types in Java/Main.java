public class Main {

    public static void main(String[] args) {
        byte num1 = 12; // Size = 1 byte
        System.out.println(num1);

        short num2 = 345; // Size = 2 bytes
        System.out.println(num2);

        int num3 = 1567;  // Size = 4 bytes
        System.out.println(num3);

        long num4 = 123456779L;  // Size = 8 bytes
        System.out.println(num4);

        boolean isMale = false; // Size = 1 byte
        System.out.println(isMale);

        char letter = 'A';  // Size = 2 bytes
        System.out.println(letter);

        float a = 12.34f; // Size = 4 bytes
        System.out.println(a);

        double b = 45.78;  // Size = 8 bytes;
        System.out.println(b);

        double c = 45.78d;
        System.out.println(b);


    }
}